# Python Program - Find Largest of Two Numbers
num1=int(input("Enter Your First Number :- "))
num2=int(input("Enter Your 2nd Number :- "))

if num1>num2:
    print(F"Largest of the given two numbers is :- [{num1}]")
elif num1==num2:
    print(F"Given two numbers are Equal :- {num1} = {num2}")
else:
    print(f"Largest of the given two numbers is :- [{num2}] ")